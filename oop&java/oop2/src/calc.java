public class calc {
    // static class " can use this class without create obj"
    public static int add(int num1,int num2){
        return num1+num2;
    }

    public static int sub(int num1,int num2){
        return num1-num2;
    }

    public static int multi(int num1,int num2){
        return num1*num2;
    }
}
